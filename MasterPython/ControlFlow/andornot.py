#  ============================================================================================================================================
#  OR
#  ============================================================================================================================================

for x in range(20):
    if x % 3 == 0 or x % 5 == 0:
        if x != 0:
            print(x)
print()

#  ============================================================================================================================================
#  AND
#  ============================================================================================================================================
for x in range(20):
    if x % 3 == 0 and x % 5 == 0 and x != 0:
        print(x)

